namespace Dreamcast.Core
{
    /// <summary>
    ///     Contains all mouse buttons.
    /// </summary>
    public enum MouseButton : byte
    {
        /// <summary>
        ///     Left mouse button.
        /// </summary>
        Left = 0,

        /// <summary>
        ///     Right mouse button.
        /// </summary>
        Right = 1,

        /// <summary>
        ///     Middle mouse button.
        /// </summary>
        Middle = 2
    }
}